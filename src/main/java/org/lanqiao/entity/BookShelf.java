package org.lanqiao.entity;

public class BookShelf {
    private Integer shelfId;

    private Integer shelfBookFlag;

    private Integer shelfUserId;

    private Integer shelfBookId;

    public Integer getShelfId() {
        return shelfId;
    }

    public void setShelfId(Integer shelfId) {
        this.shelfId = shelfId;
    }

    public Integer getShelfBookFlag() {
        return shelfBookFlag;
    }

    public void setShelfBookFlag(Integer shelfBookFlag) {
        this.shelfBookFlag = shelfBookFlag;
    }

    public Integer getShelfUserId() {
        return shelfUserId;
    }

    public void setShelfUserId(Integer shelfUserId) {
        this.shelfUserId = shelfUserId;
    }

    public Integer getShelfBookId() {
        return shelfBookId;
    }

    public void setShelfBookId(Integer shelfBookId) {
        this.shelfBookId = shelfBookId;
    }
}
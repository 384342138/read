package org.lanqiao.vo;


public class SelectTypeVo {
    private Integer bookTypeId;
    private Integer bookFee;
    private Integer bookFlag;
    private Double  bookWord;

    public Integer getBookTypeId() {
        return bookTypeId;
    }

    public void setBookTypeId(Integer bookTypeId) {
        this.bookTypeId = bookTypeId;
    }

    public Integer getBookFee() {
        return bookFee;
    }

    public void setBookFee(Integer bookFee) {
        this.bookFee = bookFee;
    }

    public Integer getBookFlag() {
        return bookFlag;
    }

    public void setBookFlag(Integer bookFlag) {
        this.bookFlag = bookFlag;
    }

    public Double getBookWord() {
        return bookWord;
    }

    public void setBookWord(Double bookWord) {
        this.bookWord = bookWord;
    }
}
